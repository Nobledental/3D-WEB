export function translatePage(lang) {
    if (!Translations[lang]) return;

    const t = Translations[lang];
    const elements = document.querySelectorAll('[data-i18n]');

    // 1. Data Attribute Replacement (Robust)
    elements.forEach(el => {
        const key = el.dataset.i18n;
        if (t[key]) el.innerText = t[key];
    });

    // 2. Fallback: Text Content Replacement (Legacy/Quick)
    // Useful for elements where we haven't added data-i18n yet
    const map = {
        '.menu-label': {
            'Dashboard': t.dashboard, 'Health': t.health, 'Organs': t.organs,
            'Labs': t.labs, 'Hospitals': t.hospitals, 'Devices': t.devices
        },
        '.card-title': {
            'Heart Rate': t.heart_rate, 'Blood Pressure': t.blood_pressure,
            'Temperature': t.temperature, 'Weight': t.weight, 'Glucose': t.glucose,
            'Oxygen Saturation': t.oxygen
        }
    };

    // Generic replacer for sidebar
    document.querySelectorAll('.menu-label').forEach(el => {
        Object.keys(map['.menu-label']).forEach(eng => {
            if (el.innerText.includes(eng)) el.innerText = map['.menu-label'][eng];
        });
    });

    // Generic replacer for cards
    document.querySelectorAll('.card-title').forEach(el => {
        Object.keys(map['.card-title']).forEach(eng => {
            if (el.innerText.includes(eng)) el.innerText = map['.card-title'][eng];
        });
    });

    // Update HTML lang attribute
    document.documentElement.lang = lang;
    localStorage.setItem('hf_lang', lang);
}
